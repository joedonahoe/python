# Given the starting variables defined, write the value and type of the subsequent expressions.  

width = 17
height = 12.0 

print( 'width/2  ' + '     Value=' + str(width/2  ) + '     Type=' + str(type(width/2  )) )
print( 'width/2.0' + '     Value=' + str(width/2.0) + '     Type=' + str(type(width/2.0)) )
print( 'height/3 ' + '     Value=' + str(height/3 ) + '     Type=' + str(type(height/3 )) )
print( '1 + 2 * 5' + '     Value=' + str(1 + 2 * 5) + '     Type=' + str(type(1 + 2 * 5)) )